
const shirt1 = {
    shirt: "tshirt",
    color: "blue",
    quantity: "1",
    size: "large",
    like: true
}
// bracket notation
const shirtSize = shirt1["size"];
console.log("size: ", shirtSize);

// dot notation
const shirtLike = shirt1.like;
console.log("Like: ", shirtLike);

const shirtColor = shirt1["color"];
console.log("Color: ", shirtColor);

